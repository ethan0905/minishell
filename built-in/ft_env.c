#include "built-in.h"

int	ft_env(char **env)
{
	int i;
	int j;

	i = 0;
	while (env[i])
	{
		j = 0;
		while (env[i][j])
		{
			printf("%c", env[i][j]);
			j++;
		}
		printf("\n");
		i++;
	}
	return (0);
}
